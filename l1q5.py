#Faça um programa que converta metros para centímetros.
#1m tem 100cm
#entrada
tamanho_em_metros = float (input('Digite o tamanho em metros: '))

#cálculo
tamanho_em_centímetros = tamanho_em_metros * 100

#exibir
print('{} metros é igual a {} centímetros'.format(tamanho_em_metros, tamanho_em_centímetros))